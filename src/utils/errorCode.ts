/**
 * 获取错误提示信息
 * @param code 错误码
 * @param context 业务场景上下文（可选）
 * @param fallback 后备提示信息（可选）
 * @returns 错误提示信息
 */
export function getErrorMessage(
  code: string | number,
  context?: string,
  fallback?: string
): string {
  const codeStr = String(code)

  // 1. 优先使用场景特定的映射
  if (context && CONTEXT_ERROR_MAP[context]?.[codeStr]) {
    return CONTEXT_ERROR_MAP[context][codeStr]
  }

  // 2. 使用通用映射
  if (ERROR_CODE_MAP[codeStr]) {
    return ERROR_CODE_MAP[codeStr]
  }

  // 3. 使用后备信息
  if (fallback) {
    return fallback
  }

  // 4. 默认提示
  return '操作失败，请稍后重试'
}

